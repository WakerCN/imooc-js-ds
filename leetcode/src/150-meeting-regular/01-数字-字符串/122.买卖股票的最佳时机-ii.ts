// @lc code=start
function maxProfit(prices: number[]): number {
  let profit = 0;
  if (prices.length < 1) {
    return profit;
  }
  for (let i = 1; i < prices.length; i++) {
    if (prices[i] > prices[i - 1]) {
      profit += prices[i] - prices[i - 1];
    }
  }
  return profit;
}
